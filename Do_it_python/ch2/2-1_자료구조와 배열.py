tuple10 = tuple('ABC')          #('A', 'B', 'C') 문자열의 각 문자로부터 원소를 생성
tuple11 = tuple([1, 2, 3])      #(1, 2, 3) 리스트로부터 원소를 생성
tuple12 = tuple({1, 2, 3})      #(1, 2, 3) 집합으로부터 원소를 생성

#range()함수를 이용
tuple13 = tuple(range(7))
