#리스트의 기초
#리스트는 원소를 변경할 수 있는 뮤터블형 객체
list01 = []
list02 = [1, 2, 3]
list03 = ['A', 'B', 'C',] #맨 마지막 원소에 쉼표를 써도 됨
list04 = list() #빈 리스트 생성
list05 = list('ABC') #['A', 'B', 'C']문자열의 각 문자로부터 원소를 생성
list06 = list([1, 2, 3]) #리스트로부터 원소를 생성
list07 = list((1, 2, 3)) #튜플로부터 원소를 생성
list08 = list({1, 2, 3}) #집합으로부터 원소를 생성
list09 = list(range(7))
list10 = list(range(3, 8))
list11 = list(range(3, 13, 2))
list12 = [None] * 5 #원솟값을 정하지 않는 리스트는 None을 사용하면 만들 수 있음

#튜플의 기초
#튜플은 원소에 순서를 매겨 결합한 것으로 원소를 변경할 수 없는 이뮤터블 자료형
#튜플은 리스트와 다르게 결합 연산자 ()를 생략할 수 있음
tuple01 = ()
tuple02 = 1,     #(1,)
tuple03 = (1,)   #(1,)  #tuple02, tuple03처럼 원소가 1개인 경우 원소 뒤에 쉼표를 반드시 입력해야 함, 쉼표가 없으면 튜플이 아니라 단순 변수로 여기기 때문
tuple04 = 1, 2, 3 
tuple05 = 1, 2, 3,
tuple06 = (1, 2, 3)
tuple07 = (1, 2, 3, )
